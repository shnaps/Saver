public class WrongImage {
    private int key;
    private String record;

    public WrongImage(int key, String record) {
        this.key = key;
        this.record = record;
    }

    public int getKey() {

        return key;
    }

    public void setKey(int key) {
        this.key = key;
    }

    public String getRecord() {
        return record;
    }

    public void setRecord(String record) {
        this.record = record;
    }
}
